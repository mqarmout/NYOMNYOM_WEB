// ── Core component ─────────────────────────────────────────────────────────
export function Px({ name, size = 14, className = '', style, ...props }) {
  const C = ICON_COMPONENTS[name];
  if (!C) {
    // graceful fallback: renders legacy emoji strings as text
    return <span style={{ fontSize: size * 0.85, lineHeight: 1, ...style }} className={className}>{name}</span>;
  }
  return (
    <C
      width={size}
      height={size}
      className={`px-icon${className ? ' ' + className : ''}`}
      style={style}
      aria-hidden="true"
      {...props}
    />
  );
}

